import UIKit
import Foundation

extension UIColor {
    
    static func colorBlueLight() -> UIColor {
        return UIColor(red: 118/255, green: 173/255, blue: 182/255, alpha: 1)
    }
    static func colorBlueDark() -> UIColor {
        return UIColor(red: 51/255, green: 56/255, blue: 75/255, alpha: 1)
    }
}


